     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required'
        ]);
        $author = new Author([
            'name' => trim($request->get('name'))
        ]);
        $author->save();
        return redirect('/authors/create')->with('success', 'author saved!');
    }

